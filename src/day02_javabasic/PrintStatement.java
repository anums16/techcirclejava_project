package day02_javabasic;

public class PrintStatement {

	public static void main(String args[]) {
		// println will display the information in the new line
		System.out.println("1");
		System.out.println("2");
		System.out.println("3");

		System.out.print(1);
		System.out.print(2);
		System.out.print(3);

		System.out.println();

		System.out.println("This is a square!");
		System.out.println();
		System.out.println(" ----------");
		System.out.println("|          |");
		System.out.println("|          |");
		System.out.println("|          |");
		System.out.println("|          |");
		System.out.println("|          |");
		System.out.println(" ----------");

		// Write a java code to display triangle on the screen.
		System.out.println("    / \\");
		System.out.println("   /   \\");
		System.out.println("  /     \\");
		System.out.println(" /       \\");
		System.out.println("/         \\");
		System.out.println("-----------");

	}

}
